// ---- Helper functions ----

function resolveLocale(locale: string): string {
  // Try exact match, then base language, then English
  if (ECHARTS_NAMES[locale]) return locale
  const base = locale.split('-')[0]
  if (ECHARTS_NAMES[base]) return base
  return 'en'
}

export function getEChartsTemplateName(locale: string, id: string, fallback: string): string {
  const loc = resolveLocale(locale)
  return ECHARTS_NAMES[loc]?.[id] ?? ECHARTS_NAMES.en[id] ?? fallback
}

